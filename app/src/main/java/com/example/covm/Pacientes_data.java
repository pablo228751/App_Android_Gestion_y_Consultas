package com.example.covm;

public class Pacientes_data {
    private String pNombre;
    private String pTelefono;
    private String pMail;
    private String pNos;
    private String pDir;
    private Integer pCuenta;
    private String pAfi;
    private String pSocial;

    public void setpNombre(String pNombre){this.pNombre=pNombre;}
    public void setpTelefono(String pTelefono){this.pTelefono=pTelefono;}
    public void setpMail(String pMail){this.pMail=pMail;}
    public void setpNos(String pNos){this.pNos=pNos;}
    public void setpDir(String pDir){this.pDir=pDir;}
    public void setpCuenta(Integer pCuenta){this.pCuenta=pCuenta;}
    public void setpAfi(String pAfi){this.pAfi=pAfi;}
    public void setpSocial(String pSocial){this.pSocial=pSocial;}

    public String getpNombre(){return pNombre;}
    public String getpTelefono(){return pTelefono;}
    public String getpMail(){return pMail;}
    public String getpNos(){return pNos;}
    public String getpDir(){return pDir;}
    public Integer getpCuenta(){return pCuenta;}
    public String getpAfi(){return pAfi;}
    public String getpSocial(){return pSocial;}
}
