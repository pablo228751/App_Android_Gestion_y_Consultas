package com.example.covm;
public class Act_data {
    private String pDetalle;
    private String pFecha;
    private String pOrigen;
    private String pMom;
    private Integer pRegistro;

    public void setpDetalle(String pDetalle){this.pDetalle=pDetalle;}
    public void setpFecha(String pFecha){this.pFecha=pFecha;}
    public void setpOrigen(String pOrigen){this.pOrigen=pOrigen;}
    public void setpMom(String pMom){this.pMom=pMom;}
    public void setpRegistro(Integer pRegistro){this.pRegistro=pRegistro;}

    public String getpDetalle(){return pDetalle;}
    public String getpFecha(){return pFecha;}
    public String getpOrigen(){return pOrigen;}
    public String getpMom(){return pMom;}
    public Integer getpRegistro(){return pRegistro;}

}
